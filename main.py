# Basic Calculator

class Add:
    def __init__(self):
        pass

    def add(self, a, b):
        return a + b


class Subtract:
    def __init__(self):
        pass

    def subtract(self, a, b):
        return a - b


class Divide:
    def __init__(self):
        pass

    def Divide(self, a, b):
        return a / b


class Multiply:
    def __init__(self):
        pass

    def Multiply(self, a, b):
        return a * b
